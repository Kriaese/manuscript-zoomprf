function FigHandle = ss_zoomprf_main_dispsine
%
% Displays sine wave at 25 Hz used for tactile stimulation.
%
% ------------------------------------------------------------------------------
% Inputs
% ------------------------------------------------------------------------------
% -/-
% ------------------------------------------------------------------------------
% Outputs
% ------------------------------------------------------------------------------
% FigHandle - Figuer handle 
% ------------------------------------------------------------------------------
% 31/08/2023: Regenerated (SS)
% 31/08/2023: Last modified (SS)
% ------------------------------------------------------------------------------

%% .............................................................................Specify sine wave 

% Frequency [Hz]
F = 25; 
% Time 
T = (0:1/(F*100):1);
% Amplitude [V]
A = 1; 
% Phase
Phi = pi; 
% Response
Y = A*sin(2*pi*F*T+Phi);

%% .............................................................................Plot sine wave against time 

plot(T,Y, 'Color', [0,0,0], 'LineWidth', 1)

%% .............................................................................Remove tick labels

set(gca,'YTickLabel',[]);
set(gca,'XTickLabel',[]);

%% .............................................................................Remove axes

set(gca,'XColor', 'none','YColor','none'); 

%% .............................................................................Get figure handle

FigHandle = gcf;

end 